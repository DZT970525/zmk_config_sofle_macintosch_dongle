// ============== Trigram patterns ==============
// Direction + trigram bit pattern
// Standard Bagua: 乾(0,0,0)→南→S(top), 坤(1,1,1)→北→N(bottom)
// and E→离(1,0,1), W→坎(0,1,1), SE→兑(1,0,0), NE→震(0,0,1),
// NW→艮(1,1,0), SW→巽(0,1,0)

// Ring radius from center (120, 120) in creative area (y=42~180)
#define RING_R 60
#define CX_OFF 0
#define CY_OFF 0

typedef struct {
    int8_t dx;
    int8_t dy;
    uint8_t pattern; // 3 bits: line0_solid=0/broken=1, line1, line2 (top→bottom)
} TriPos;

static const TriPos trigrams[8] = {
    {  0, -70, 0b101 }, // 上 S  - 离 LI   - 火 ╋━╋

    {  50, -50, 0b111 }, // 右上 SW- 坤 KUN  - 地 ╋╋╋

    {  70,   0, 0b100 }, // 右 W  - 兑 DUI  - 泽 ╋━━

    {  50,  50, 0b000 }, // 右下 NW- 乾 QIAN - 天 ━━━

    {  0,   70, 0b010 }, // 下 N  - 坎 KAN  - 水 ━╋━

    { -50,  50, 0b011 }, // 左下 NE- 艮 GEN  - 山 ━╋╋

    { -70,   0, 0b110 }, // 左 E  - 震 ZHEN - 雷 ╋╋━

    { -50, -50, 0b001 }, // 左上 SE- 巽 XUN  - 风 ━━╋
};

