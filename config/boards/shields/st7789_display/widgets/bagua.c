// ============== Drawing helpers ==============

static void draw_h_line(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color_565) {
    if (w == 0 || h == 0 || w > LINE_BUF_W) return;
    fill_buffer_color(line_buf, w * h * 2, color_565);
    render_filled_rectangle(line_buf, x, y, w, h);
}

static void draw_v_bar(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color_565) {
    if (w == 0 || h == 0) return;
    size_t buf_sz = w * h * 2;
    if (buf_sz > sizeof(line_big_buf)) return;
    fill_buffer_color(line_big_buf, buf_sz, color_565);
    render_filled_rectangle(line_big_buf, x, y, w, h);
}

/**
 * Draw one trigram: 3 horizontal lines at (cx, cy).
 * pattern: 3-bit (MSB=top, LSB=bottom, 0=solid ---, 1=broken - -)
 */
// ============== Trigram rotation ==============
// Rotate trigram so top points outward along radial direction
// Q10 fixed-point (1024 = 1.0)
static const int16_t trigram_cos[8] = {
    1024, 724, 0, -724, -1024, -724, 0, 724
};
static const int16_t trigram_sin[8] = {
    0, 724, 1024, 724, 0, -724, -1024, -724
};

#define TMP_BUF_W 40


#define TMP_BUF_H 40


#define TMP_RC 20



static uint16_t *trigram_horiz;
static uint16_t *trigram_rot;
static uint16_t *taichi_rotated;  // rotated Tai Chi buffer

/**
 * Draw a horizontal trigram into the 1bpp temp buffer at buffer center.
 * 0 = transparent, 1 = foreground pixel
 */
static void draw_one_trigram_horiz(uint16_t *buf, uint8_t pattern, bool half) {
    memset(buf, 0, TMP_BUF_W * TMP_BUF_H * sizeof(uint16_t));

    int half_w = 11;
    int gap = 4;
    int thick = 3;
    int spc = 6;

    for (int i = 0; i < 3; i++) {
        if (half && i == 1) continue;  // half trigram: skip middle line
        int ly = TMP_RC - 6 + i * (thick + spc);


        uint8_t bit = (pattern >> (2 - i)) & 1;

        if (bit == 0) {
            for (int yy = ly; yy < ly + thick; yy++) {
                for (int xx = TMP_RC - half_w; xx < TMP_RC + half_w; xx++) {
                    if (xx >= 0 && xx < TMP_BUF_W && yy >= 0 && yy < TMP_BUF_H)
                        buf[yy * TMP_BUF_W + xx] = 1;
                }
            }
        } else {
            int seg_w = half_w - gap;
            for (int yy = ly; yy < ly + thick; yy++) {
                for (int xx = TMP_RC - half_w; xx < TMP_RC - half_w + seg_w; xx++) {
                    if (xx >= 0 && xx < TMP_BUF_W && yy >= 0 && yy < TMP_BUF_H)
                        buf[yy * TMP_BUF_W + xx] = 1;
                }
                for (int xx = TMP_RC + gap; xx < TMP_RC + gap + seg_w; xx++) {
                    if (xx >= 0 && xx < TMP_BUF_W && yy >= 0 && yy < TMP_BUF_H)
                        buf[yy * TMP_BUF_W + xx] = 1;
                }
            }
        }
    }
}

/**
 * Rotate src bitmap clockwise by angle θ (cos_a, sin_a in Q10) into dst.
 * Uses inverse mapping (CCW) with rounding to avoid pixel gaps.
 */
/**
 * Generic bitmap rotation for arbitrary size (used for Tai Chi).
 */
static void rotate_bitmap_generic(const uint16_t *src, uint16_t *dst,
                                   uint16_t w, uint16_t h, uint16_t rc,
                                   int16_t cos_a, int16_t sin_a) {
    memset(dst, 0, w * h * sizeof(uint16_t));
    for (int by = 0; by < h; by++) {
        for (int bx = 0; bx < w; bx++) {
            int dx = bx - rc;
            int dy = by - rc;
            int sx = rc + (dx * cos_a + dy * sin_a + 512) / 1024;
            int sy = rc + (-dx * sin_a + dy * cos_a + 512) / 1024;
            if (sx >= 0 && sx < (int)w && sy >= 0 && sy < (int)h) {
                dst[by * w + bx] = src[sy * w + sx];
            }
        }
    }
}

static void rotate_trigram(const uint16_t *src, uint16_t *dst,
                            int16_t cos_a, int16_t sin_a) {
    memset(dst, 0, TMP_BUF_W * TMP_BUF_H * sizeof(uint16_t));

    for (int by = 0; by < TMP_BUF_H; by++) {
        for (int bx = 0; bx < TMP_BUF_W; bx++) {
            int dx = bx - TMP_RC;
            int dy = by - TMP_RC;

            int sx = TMP_RC + (dx * cos_a + dy * sin_a + 512) / 1024;

            int sy = TMP_RC + (-dx * sin_a + dy * cos_a + 512) / 1024;


            if (sx >= 0 && sx < TMP_BUF_W && sy >= 0 && sy < TMP_BUF_H) {
                dst[by * TMP_BUF_W + bx] = src[sy * TMP_BUF_W + sx];
            }
        }
    }
}



// ============== BT Profile color control ==============
static uint8_t active_profile = 0;

static bool usb_connected = false;
static bool num_lock_on = false;
static bool caps_lock_on = false;
static bool profile_connected = false;
static bool profile_bonded = false;
static bool blink_visible = true;
static bool blink_active = false;
static bool half_trigram = false;
static struct k_work_delayable bagua_blink_work;
static lv_timer_t *bagua_redraw_timer;
static uint8_t active_layer = 0;

static void bagua_redraw_cb(lv_timer_t *timer) {
    draw_bagua();
    lv_timer_pause(timer);
}

static void bagua_blink_handler(struct k_work *work) {
    blink_visible = !blink_visible;
    draw_bagua();
    if (blink_active) {
        k_work_schedule(&bagua_blink_work, K_MSEC(500));
    }
}

void bagua_set_active_profile(uint8_t profile) {
    if (profile > 7) return;
    active_profile = profile;
    if (bagua_redraw_timer) lv_timer_resume(bagua_redraw_timer);
}

void bagua_set_usb_connected(bool connected) {
    usb_connected = connected;
    if (bagua_redraw_timer) lv_timer_resume(bagua_redraw_timer);
}

void bagua_set_num_lock(bool on) {
    num_lock_on = on;
    if (bagua_redraw_timer) lv_timer_resume(bagua_redraw_timer);
}

void bagua_set_profile_state(bool connected, bool bonded) {
    profile_connected = connected;
    profile_bonded = bonded;
    half_trigram = bonded && !connected;
    bool was_active = blink_active;
    blink_active = !connected && !bonded;
    if (blink_active && !was_active) {
        blink_visible = true;
        k_work_schedule(&bagua_blink_work, K_MSEC(500));
    } else if (!blink_active && was_active) {
        blink_visible = true;
        k_work_cancel_delayable(&bagua_blink_work);
    }
}

void bagua_set_caps_lock(bool on) {
    caps_lock_on = on;
    if (bagua_redraw_timer) lv_timer_resume(bagua_redraw_timer);
}

void bagua_set_layer(uint8_t layer) {
    active_layer = layer;
    if (bagua_redraw_timer) lv_timer_resume(bagua_redraw_timer);
}

static int bagua_layer_listener_cb(const zmk_event_t *eh) {
    active_layer = zmk_keymap_highest_layer_active();
    if (bagua_redraw_timer) lv_timer_resume(bagua_redraw_timer);
    return 0;
}

ZMK_LISTENER(bagua_layer_status, bagua_layer_listener_cb);
ZMK_SUBSCRIPTION(bagua_layer_status, zmk_layer_state_changed);

// ============== HID indicators (NumLock/CapsLock) ==============
static int bagua_hid_listener_cb(const zmk_event_t *eh) {
    const struct zmk_hid_indicators_changed *ev = as_zmk_hid_indicators_changed(eh);
    if (!ev) return 0;
    num_lock_on = (ev->indicators & 0x01) != 0;
    caps_lock_on = (ev->indicators & 0x02) != 0;
    if (bagua_redraw_timer) lv_timer_resume(bagua_redraw_timer);
    return 0;
}

ZMK_LISTENER(bagua_hid_status, bagua_hid_listener_cb);
ZMK_SUBSCRIPTION(bagua_hid_status, zmk_hid_indicators_changed);

// Profile colors (RGB565): 0=red, 1=yellow, 2=green, 3=cyan, 4=magenta
static const uint16_t profile_colors[8] = {
    0xF800, // red      - 离 (top)
    0xFFE0, // yellow   - 坤 (upper-right)
    0xFFFF, // white    - 兑 (right)
    0xFEC0, // gold     - 乾 (lower-right)
    0x001F, // blue     - 坎 (bottom)
    0xA140, // brown    - 艮 (lower-left)
    0xF81F, // purple   - 震 (left)
    0x07FF  // cyan     - 巽 (upper-left)
};
static const uint16_t gray_color = 0x8410; // inactive trigram color

// ============== Main drawing function ==============

void draw_bagua(void) {
    if (!bagua_initialized) return;

    uint16_t scale = 1;
    uint16_t tc_x = 120 - (TAICHI_W * scale / 2);
    uint16_t tc_y = 120 - (TAICHI_H * scale / 2);

    uint16_t fg = get_theme_font_color();
    uint16_t bg = get_theme_font_bg_color();
    uint16_t tri_fg = get_theme_font_color();

    // Rotate Tai Chi based on active layer
    // Layer 0(3)=0°, 1(#)=45°, 2(F)=90°, 3(M)=135°
    static const int16_t layer_cos[4] = {1024, 724, 0, -724};
    static const int16_t layer_sin[4] = {0, 724, 1024, 724};
    uint8_t tc_angle_idx = (active_layer < 4) ? active_layer : 0;
    
    rotate_bitmap_generic(taichi_bitmap, taichi_rotated,
                           TAICHI_W, TAICHI_H, TAICHI_H/2,
                           layer_cos[tc_angle_idx], layer_sin[tc_angle_idx]);
    
    render_bitmap(scaled_bitmap_taichi, taichi_rotated,
                  tc_x, tc_y,
                  TAICHI_W, TAICHI_H, scale,
                  fg, bg);

    // === BT Profile trigrams (0-4) ===
    for (int i = 0; i < 5; i++) {
        uint16_t color = (i == active_profile)
            ? ((blink_active && !blink_visible) ? gray_color : profile_colors[i])
            : gray_color;
        bool do_half = (i == active_profile) && half_trigram;
        draw_one_trigram_horiz(trigram_horiz, trigrams[i].pattern, do_half);
        rotate_trigram(trigram_horiz, trigram_rot, trigram_cos[i], trigram_sin[i]);
        uint16_t tx = 120 + trigrams[i].dx;
        uint16_t ty = 120 + trigrams[i].dy;
        render_bitmap(trigram_rot, trigram_rot,
                      tx - TMP_RC, ty - TMP_RC, TMP_BUF_W, TMP_BUF_H, 1,
                      color, bg);
    }

    // === 艮 GEN (index 5) - USB indicator ===
    {
        uint16_t color = usb_connected ? profile_colors[5] : gray_color;
        draw_one_trigram_horiz(trigram_horiz, trigrams[5].pattern, false);
        rotate_trigram(trigram_horiz, trigram_rot, trigram_cos[5], trigram_sin[5]);
        uint16_t tx = 120 + trigrams[5].dx;
        uint16_t ty = 120 + trigrams[5].dy;
        render_bitmap(trigram_rot, trigram_rot,
                      tx - TMP_RC, ty - TMP_RC, TMP_BUF_W, TMP_BUF_H, 1,
                      color, bg);
    }

    // === 震 ZHEN (index 6) - CapsLock indicator ===
    {
        uint16_t color = caps_lock_on ? profile_colors[6] : gray_color;
        draw_one_trigram_horiz(trigram_horiz, trigrams[6].pattern, false);
        rotate_trigram(trigram_horiz, trigram_rot, trigram_cos[6], trigram_sin[6]);
        uint16_t tx = 120 + trigrams[6].dx;
        uint16_t ty = 120 + trigrams[6].dy;
        render_bitmap(trigram_rot, trigram_rot,
                      tx - TMP_RC, ty - TMP_RC, TMP_BUF_W, TMP_BUF_H, 1,
                      color, bg);
    }

    // === 巽 XUN (index 7) - NumLock indicator ===
    {
        uint16_t color = num_lock_on ? profile_colors[7] : gray_color;
        draw_one_trigram_horiz(trigram_horiz, trigrams[7].pattern, false);
        rotate_trigram(trigram_horiz, trigram_rot, trigram_cos[7], trigram_sin[7]);
        uint16_t tx = 120 + trigrams[7].dx;
        uint16_t ty = 120 + trigrams[7].dy;
        render_bitmap(trigram_rot, trigram_rot,
                      tx - TMP_RC, ty - TMP_RC, TMP_BUF_W, TMP_BUF_H, 1,
                      color, bg);
    }

}

// ============== Init / lifecycle ==============

void zmk_widget_bagua_init(void) {
    size_t row_buf = TAICHI_W * TAICHI_H;
    scaled_bitmap_taichi = k_malloc(row_buf * sizeof(uint16_t));
    trigram_horiz = k_malloc(TMP_BUF_W * TMP_BUF_H * sizeof(uint16_t));
    trigram_rot = k_malloc(TMP_BUF_W * TMP_BUF_H * sizeof(uint16_t));
    taichi_rotated = k_malloc(TAICHI_W * TAICHI_H * sizeof(uint16_t));
    k_work_init_delayable(&bagua_blink_work, bagua_blink_handler);
    bagua_redraw_timer = lv_timer_create(bagua_redraw_cb, 1, NULL);
    if (bagua_redraw_timer) lv_timer_pause(bagua_redraw_timer);
    bagua_initialized = true;
}

void start_bagua(void) {
    if (!bagua_initialized) {
        zmk_widget_bagua_init();
    }
    draw_bagua();
}

void stop_bagua(void) {}
