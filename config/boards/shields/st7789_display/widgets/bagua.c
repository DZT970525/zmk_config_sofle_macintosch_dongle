// ============== Trigram patterns ==============
// Direction + trigram bit pattern
// Standard Bagua: 乾(0,0,0)→南→S(top), 坤(1,1,1)→北→N(bottom)
// and E→离(1,0,1), W→坎(0,1,1), SE→兑(1,0,0), NE→震(0,0,1),
// NW→艮(1,1,0), SW→巽(0,1,0)

// Ring radius from center (120, 111) in creative area (y=42~180)
#define RING_R 54
#define CX_OFF 0
#define CY_OFF 0

typedef struct {
    int8_t dx;
    int8_t dy;
    uint8_t pattern; // 3 bits: line0_solid=0/broken=1, line1, line2 (top→bottom)
} TriPos;

static const TriPos trigrams[8] = {
    {  0, -54, 0b000 }, // S  - 乾 QIAN - Heaven (≡≡≡)
    {  38, -38, 0b100 }, // SE - 兑 DUI  - Lake   (::≡)
    {  54,   0, 0b101 }, // E  - 离 LI   - Fire   (:::)
    {  38,  38, 0b001 }, // NE - 震 ZHEN - Thunder (≡≡:)
    {  0,   54, 0b111 }, // N  - 坤 KUN  - Earth  (:::)
    { -38,  38, 0b011 }, // NW - 艮 GEN  - Mountain (≡::)
    { -54,   0, 0b110 }, // W  - 坎 KAN  - Water  (::≡)
    { -38, -38, 0b010 }, // SW - 巽 XUN  - Wind   (:≡:)
};

// ============== Static state ==============
static uint16_t *scaled_bitmap_taichi;
static bool bagua_initialized = false;

// Scratch buffer for drawing small horizontal line segments (trigrams)
#define LINE_BUF_W 16
#define LINE_BUF_H 2
static uint8_t line_buf[LINE_BUF_W * LINE_BUF_H * 2];
static uint8_t line_big_buf[256 * 8 * 2]; // for larger fills

// ============== Drawing helpers ==============

static void draw_h_line(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color_565) {
    if (w == 0 || h == 0 || w > LINE_BUF_W) return;
    fill_buffer_color(line_buf, w * h * 2, color_565);
    render_filled_rectangle(line_buf, x, y, w, h);
}

static void draw_v_bar(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color_565) {
    if (w == 0 || h == 0) return;
    size_t buf_sz = w * h * 2;
    if (buf_sz > sizeof(line_big_buf)) return;
    fill_buffer_color(line_big_buf, buf_sz, color_565);
    render_filled_rectangle(line_big_buf, x, y, w, h);
}

/**
 * Draw one trigram: 3 horizontal lines at (cx, cy).
 * pattern: 3-bit (MSB=top, LSB=bottom, 0=solid ---, 1=broken - -)
 */
static void draw_one_trigram(uint16_t cx, uint16_t cy, uint8_t pattern, uint16_t fg, uint16_t bg) {
    uint16_t half_w = 7;  // 14px total width
    uint16_t gap = 3;     // gap in broken lines
    uint16_t thick = 2;   // line thickness
    uint16_t spc = 5;     // space between lines

    (void)bg;

    for (int i = 0; i < 3; i++) {
        uint16_t ly = cy - 6 + i * (thick + spc);
        if (ly < 42 || ly > 178) continue;

        uint8_t bit = (pattern >> (2 - i)) & 1;
        if (bit == 0) {
            // Solid line: ---
            draw_h_line(cx - half_w, ly, half_w * 2, thick, fg);
        } else {
            // Broken line: - -
            uint16_t seg_w = half_w - gap;
            draw_h_line(cx - half_w, ly, seg_w, thick, fg);
            if (seg_w > 0) draw_h_line(cx + gap, ly, seg_w, thick, fg);
        }
    }
}

// ============== Main drawing function ==============

void draw_bagua(void) {
    if (!bagua_initialized) return;

    uint16_t scale = 1;
    uint16_t tc_x = 120 - (TAICHI_W * scale / 2);
    uint16_t tc_y = 111 - (TAICHI_H * scale / 2);

    uint16_t fg = get_theme_font_color();
    uint16_t bg = get_theme_font_bg_color();
    uint16_t tri_fg = get_theme_font_color();

    // Draw Tai Chi (single call to render_bitmap with the full 30×30 array)
    render_bitmap(scaled_bitmap_taichi, (uint16_t *)taichi_bitmap,
                  tc_x, tc_y,
                  TAICHI_W, TAICHI_H, scale,
                  fg, bg);

    // Draw 8 trigrams in a ring
    for (int i = 0; i < 8; i++) {
        uint16_t tx = 120 + trigrams[i].dx;
        uint16_t ty = 111 + trigrams[i].dy;
        draw_one_trigram(tx, ty, trigrams[i].pattern, tri_fg, bg);
    }
}

// ============== Init / lifecycle ==============

void zmk_widget_bagua_init(void) {
    size_t row_buf = TAICHI_W * TAICHI_H;
    scaled_bitmap_taichi = k_malloc(row_buf * sizeof(uint16_t));
    bagua_initialized = true;
}

void start_bagua(void) {
    if (!bagua_initialized) {
        zmk_widget_bagua_init();
    }
    draw_bagua();
}

void stop_bagua(void) {}
